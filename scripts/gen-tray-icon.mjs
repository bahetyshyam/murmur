// Generates the menubar tray glyph as a base64 PNG data URL, with no image
// dependencies (pure Node zlib). Writes src/main/trayIcon.ts. This is a
// PLACEHOLDER ring glyph for the Electron menubar shell (Phase A); the real
// icon arrives in the design-system pass. Grayscale+alpha (PNG color type 4),
// black pixels + shaped alpha so Electron renders it as a template image
// (auto-tinted for light/dark menubars).
//
//   node scripts/gen-tray-icon.mjs
import { deflateSync, crc32 } from 'node:zlib'
import { writeFileSync } from 'node:fs'

function ringPNG(size, kind) {
  const cx = (size - 1) / 2
  const cy = (size - 1) / 2
  const outer = size * 0.42
  // Bold stroke so the template glyph is clearly visible in the menu bar (the
  // thin earlier ring was easy to miss). Idle = thick hollow ring; recording =
  // filled dot ("on").
  const ringInner = outer - Math.max(2, size * 0.22)
  // grayscale+alpha: 2 bytes/px, plus 1 filter byte per row
  const raw = Buffer.alloc(size * (1 + size * 2))
  let p = 0
  for (let y = 0; y < size; y++) {
    raw[p++] = 0 // filter: none
    for (let x = 0; x < size; x++) {
      const d = Math.hypot(x - cx, y - cy)
      let a = 0
      if (kind === 'recording') {
        // filled dot
        if (d <= outer) a = 255
      } else {
        // hollow ring
        if (d <= outer && d >= ringInner) a = 255
      }
      raw[p++] = 0      // gray (black) — template uses alpha only
      raw[p++] = a      // alpha
    }
  }
  const chunks = []
  const sig = Buffer.from([137, 80, 78, 71, 13, 10, 26, 10])
  const ihdr = Buffer.alloc(13)
  ihdr.writeUInt32BE(size, 0)
  ihdr.writeUInt32BE(size, 4)
  ihdr[8] = 8   // bit depth
  ihdr[9] = 4   // color type: grayscale + alpha
  ihdr[10] = 0; ihdr[11] = 0; ihdr[12] = 0
  chunks.push(chunk('IHDR', ihdr))
  chunks.push(chunk('IDAT', deflateSync(raw)))
  chunks.push(chunk('IEND', Buffer.alloc(0)))
  return Buffer.concat([sig, ...chunks])
}

function chunk(type, data) {
  const typeBuf = Buffer.from(type, 'ascii')
  const len = Buffer.alloc(4)
  len.writeUInt32BE(data.length, 0)
  const crcBuf = Buffer.alloc(4)
  crcBuf.writeUInt32BE(crc32(Buffer.concat([typeBuf, data])) >>> 0, 0)
  return Buffer.concat([len, typeBuf, data, crcBuf])
}

function dataURL(size, kind) {
  return 'data:image/png;base64,' + ringPNG(size, kind).toString('base64')
}

// A menubar template glyph is 18pt; HiDPI (2x) displays need a 36px @2x backing
// rep or macOS lays the status item out with HEIGHT 0 (invisible). So we emit a
// crisp glyph at BOTH 18px (@1x) and 36px (@2x) per state; trayImage() composes
// them into one multi-rep NativeImage via addRepresentation (never resize()).
const out = `// AUTO-GENERATED by scripts/gen-tray-icon.mjs — do not edit by hand.
// Placeholder menubar template glyphs (Phase A). Replaced in the design pass.
// Each state ships an @1x (18px) AND @2x (36px) rep so the status item draws on
// HiDPI menu bars (a single-scale image lays out with height 0 → invisible).
export const TRAY_ICON_IDLE_1X = '${dataURL(18, 'idle')}'
export const TRAY_ICON_IDLE_2X = '${dataURL(36, 'idle')}'
export const TRAY_ICON_RECORDING_1X = '${dataURL(18, 'recording')}'
export const TRAY_ICON_RECORDING_2X = '${dataURL(36, 'recording')}'
`
writeFileSync(new URL('../src/main/trayIcon.ts', import.meta.url), out)
console.log('wrote src/main/trayIcon.ts')
